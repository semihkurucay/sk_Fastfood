    private void btnProductAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProductAddActionPerformed
        // TODO add your handling code here:
        try {
            if (productInfo.chkSetComment(txtProductComment.getText()) && productProcess.isThereProduct(Integer.parseInt(txtProductID.getText()))) {
                productInfo = new SellProductInfo();

                product = productProcess.getProduct(Integer.parseInt(txtProductID.getText()));
                productInfo.setAmount((int) spbProductCount.getValue());
                productInfo.setProduct(productProcess.getProduct(Integer.parseInt(txtProductID.getText())));
                productInfo.setComment(txtProductComment.getText());
                
                productList.add(productInfo);
                model.addElement(productInfo.getProduct().getId() + " | " + productInfo.getProduct().getName() + " | x" + productInfo.getAmount() + " | " + productInfo.getComment());

            } else{
                JOptionPane.showMessageDialog(null, " | bu simge kullanılamaz.\nGirmek istediğiniz ürün sistemde kayıtlı olmaya bilir.", "Hatalı Format", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Sayı girilecek yere harf girişi yaptınız.", "Hatalı Format", JOptionPane.ERROR_MESSAGE);
        }

        lblTotalPrice.setText(totalPrice() + "");
    }//GEN-LAST:event_btnProductAddActionPerformed

