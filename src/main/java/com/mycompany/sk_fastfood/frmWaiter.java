    private void btnBuyPriceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuyPriceActionPerformed
        // TODO add your handling code here:
        if (!productList.isEmpty()) {
            try {
                float money = Float.parseFloat(txtTakenMoney.getText());

                if(money >= totalPrice()){
                    if(sellProcess.add(Float.parseFloat(txtTakenMoney.getText()), Float.parseFloat(txtGiveMoney.getText()), payType, productList)){
                        JOptionPane.showMessageDialog(null, "Sipariş başarıyla oluşturuldu, mutfağa aktarıldı.", "Sipariş Oluşturuldu", JOptionPane.INFORMATION_MESSAGE);
                    }else{
                        JOptionPane.showMessageDialog(null, "Sipariş oluşturulurken hata gerçekleşti.", "Sipariş Hatası", JOptionPane.WARNING_MESSAGE);
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "Alınana para sepet ücretini karşılamıyor!", "Eksik Para", JOptionPane.WARNING_MESSAGE);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Sayı girilecek yere harf girişi yaptınız.", "Hatalı Format", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Sepetiniz boş olduğu için satış gerçekleşmiyor.", "Boş Sepet", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnBuyPriceActionPerformed

